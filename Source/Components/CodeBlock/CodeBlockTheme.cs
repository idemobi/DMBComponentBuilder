namespace DMBComponentBuilder
{
    /// <summary>
    ///     Defines the visual themes available for a <see cref="CodeBlockBuilder" />.
    /// </summary>
    public enum CodeBlockTheme
    {
        /// <summary>
        ///     Uses the neutral documentation theme.
        /// </summary>
        Default,

        /// <summary>
        ///     Uses a bright code surface.
        /// </summary>
        Light,

        /// <summary>
        ///     Uses a dark code surface.
        /// </summary>
        Dark
    }
}