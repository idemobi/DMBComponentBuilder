namespace DMBComponentBuilder
{
    /// <summary>
    /// Defines web comic display mode options used by web comic component rendering.
    /// </summary>
    public enum WebComicDisplayMode
    {
        /// <summary>
        /// Represents the fit width option for web comic rendering.
        /// </summary>
        FitWidth,
        /// <summary>
        /// Represents the fit height option for web comic rendering.
        /// </summary>
        FitHeight,
        /// <summary>
        /// Represents the full image option for web comic rendering.
        /// </summary>
        FullImage
    }
}
