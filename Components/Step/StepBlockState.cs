namespace DMBComponentBuilder
{
    /// <summary>
    /// Defines step block state options used by step component rendering.
    /// </summary>
    public enum StepBlockState
    {
        /// <summary>
        /// Represents the future option for step rendering.
        /// </summary>
        Future,
        /// <summary>
        /// Represents the current option for step rendering.
        /// </summary>
        Current,
        /// <summary>
        /// Represents the done option for step rendering.
        /// </summary>
        Done,
        /// <summary>
        /// Represents the disabled option for step rendering.
        /// </summary>
        Disabled
    }
}
