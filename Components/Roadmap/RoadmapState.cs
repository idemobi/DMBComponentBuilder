namespace DMBComponentBuilder
{
    /// <summary>
    /// Defines roadmap state options used by roadmap component rendering.
    /// </summary>
    public enum RoadmapState
    {
        /// <summary>
        /// Represents the past option for roadmap rendering.
        /// </summary>
        Past,
        /// <summary>
        /// Represents the current option for roadmap rendering.
        /// </summary>
        Current,
        /// <summary>
        /// Represents the future option for roadmap rendering.
        /// </summary>
        Future,
        /// <summary>
        /// Represents the blocked option for roadmap rendering.
        /// </summary>
        Blocked,
        /// <summary>
        /// Represents the cancelled option for roadmap rendering.
        /// </summary>
        Cancelled
    }
}